    public void setStaffs(){
        try {
            con = DriverManager.getConnection(URL, USER, PASS);
            sql = "SELECT name FROM staff";
            pstmt = con.prepareStatement(sql);
            rs = pstmt.executeQuery();
            comboStaffs.removeAllItems();
            while(rs.next()){
                comboStaffs.addItem(rs.getString("name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FeedbackForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    // GETS THE SELECTED BUTTON FROM A BUTTON GROUP
    
    public String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }
    

