    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            con = DriverManager.getConnection(URL, USER, PASS);
            sql = "INSERT INTO ratings VALUES(?,?,?,?,?,?,?,?)";
            pstmt = con.prepareStatement(sql);
            
            String name = comboStaffs.getSelectedItem().toString();
            int pOne = scale.returnPoints(getSelectedButtonText(grp1));
            // JOptionPane.showMessageDialog(this, pOne);
            int pTwo = scale.returnPoints(getSelectedButtonText(grp2));
            int pThree = scale.returnPoints(getSelectedButtonText(grp3));
            int pFour = scale.returnPoints(getSelectedButtonText(grp4));
            int pFive = scale.returnPoints(getSelectedButtonText(grp5));
            int pSix = scale.returnPoints(getSelectedButtonText(grp6));
                    
            Float avg = Float.valueOf((pOne + pTwo + pThree + pFour + pFive + pSix)) / 6;
            
            pstmt.setString(1, name);
            pstmt.setInt(2, pOne);
            pstmt.setInt(3, pTwo);
            pstmt.setInt(4, pThree);
            pstmt.setInt(5, pFour);
            pstmt.setInt(6, pFive);
            pstmt.setInt(7, pSix);
            pstmt.setFloat(8, avg);
            pstmt.executeUpdate();
           
            clearRadioButtonSelections();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(FeedbackForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

